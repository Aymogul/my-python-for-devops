for number in range(0, 10):
    print(number)

for number in range(5, 10):
    print(number, end=' ')
    # start = 5 → begin from 5
    # stop = 10 → go up to but not including 10
    # step = 1 (default)


for number in range(0, 10, 2):
    print(number, end=' ')
    # start = 0
    # stop = stop before 10
    # step by 2




for number in range(10, 0, -2):
    print(number, end=' ')